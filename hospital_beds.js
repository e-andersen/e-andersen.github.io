function createRoomPointsAndBeds() {
    rooms.map(function (item) { roomPoints.push({ x: item.x + 15, y: item.y + 15 }) });
    var i = 0;
    colors.map(function (item) { beds.push({ x: roomPoints[i % 11].x, y: roomPoints[i % 11].y, col: item, n_x: roomPoints[i % 11].x, n_y: roomPoints[i % 11].y }); i++; });
}

function drawRooms() {
    rooms.map(function (item) { ctx.strokeRect(item.x, item.y, 150, 100); })
}

function drawBeds() {
    beds.map(function (item) {
        ctx.beginPath();
        ctx.fillStyle = item.col;
        ctx.arc(item.x, item.y, 10, 0, Math.PI * 2, true);
        ctx.closePath();
        ctx.fill();
    });
}

function draw() {
    ctx.clearRect(0, 0, 1000, 400);
    drawBeds();
    drawRooms();
}

function moveBeds() {
    //         var cur_sec = Date.now()/ 1000;

    if (cnt % emptyLoopIteration == 0) {

        var bedID = moves[moveCnt].bID;
        var roomID = moves[moveCnt].sID;

        beds[bedID].n_x = roomPoints[roomID].x + Math.floor(Math.random() * 120);
        beds[bedID].n_y = roomPoints[roomID].y + Math.floor(Math.random() * 70);

        console.log('moveCnt = ' + moveCnt + ' bedID ' + bedID + ' -> stueID ' + roomID);

        moveCnt++;
        if (moveCnt == moves.length) {
            clearInterval(conJob);
        }
        var pct = Math.round(moveCnt * 100 / moves.length);
        var duration_sec = ((moves.length - moveCnt) * iterationInterval * emptyLoopIteration) / 1000;

        var pbar = document.getElementById('proBar');
        var pcent = document.getElementById('proCent');
        var tremain = document.getElementById('timeRemain');

        pcent.innerHTML = pct + "%";
        pbar.value = pct;
        tremain.innerHTML = Math.round(duration_sec) + "sec";
    }
    cnt++;

    beds.map(function (item) { moveBed(item); });
    draw();
}

